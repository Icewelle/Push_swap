#include "libft.h"

long	ft_atol(const char *str)
{
	long	i;
	long	number;
	long	is_negative;

	i = 0;
	number = 0;
	is_negative = 1;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			is_negative *= -1;
		i++;
	}
	while (str[i] >= 48 && str[i] <= 57)
	{
		number = number * 10 + (str[i] - 48);
		i++;
	}
	return (number * is_negative);
}
